import 'dotenv/config';
import { neon } from '@neondatabase/serverless';
import { drizzle } from 'drizzle-orm/neon-http';
import { eq } from 'drizzle-orm';
import { usersTable } from './db/schema';

const sql = neon(process.env.DATABASE_URL!);
const db = drizzle({ client: sql });

async function main() {
  try {
    console.log('Starting seed operations...');
    
    const user: typeof usersTable.$inferInsert = {
      name: 'John',
      age: 30,
      email: 'john@example.com',
    };

    await db.insert(usersTable).values(user);
    console.log('âœ… New user created!');

    const users = await db.select().from(usersTable);
    console.log('ğŸ“‹ Getting all users from the database:', users);

    await db
      .update(usersTable)
      .set({
        age: 31,
      })
      .where(eq(usersTable.email, user.email));
    console.log('âœ… User info updated!');

    const updatedUsers = await db.select().from(usersTable);
    console.log('ğŸ“‹ Updated user:', updatedUsers);

    await db.delete(usersTable).where(eq(usersTable.email, user.email));
    console.log('âœ… User deleted!');
    
    console.log('\nğŸ‰ All CRUD operations completed successfully!');
  } catch (error) {
    console.error('âŒ Error:', error);
    process.exit(1);
  }
}

main();
